/**
 * Maps a weather condition string (from the API) and temperature
 * to one of our defined categories.
 * @param {string} condition - The weather condition string (e.g., "Mainly Clear", "Light Rain")
 * @param {number} temp - The current temperature in Fahrenheit
 * @returns {string} One of ['sunny', 'partlySunny', 'cloudy', 'rain', 'thunderstorm', 'snow', 'general']
 */
function mapConditionToCategory(condition, temp) {
  const conditionLower = (condition || '').toLowerCase();

  if (conditionLower.includes('thunderstorm') || conditionLower.includes('hail')) {
    return 'thunderstorm';
  }
  if (conditionLower.includes('rain') || conditionLower.includes('drizzle') || conditionLower.includes('shower')) {
    return 'rain';
  }
  if (conditionLower.includes('snow') || conditionLower.includes('sleet') || conditionLower.includes('grains')) {
    return 'snow';
  }
  if (conditionLower.includes('fog') || conditionLower.includes('overcast')) {
    return 'cloudy';
  }
  if (conditionLower.includes('partly cloudy')) {
    return 'partlySunny';
  }
  if (conditionLower.includes('clear')) {
    return 'sunny';
  }
  
  // Fallbacks based on temperature if condition is vague
  if (temp > 70) {
    return 'sunny';
  }
  if (temp < 45) {
    return 'cloudy';
  }

  return 'general';
}

/**
 * Get a weather phrase based on temperature, condition, mode, and theme
 * @param {number} temp - Temperature in Fahrenheit
 * @param {string} condition - Weather condition string (e.g., "Partly Cloudy")
 * @param {boolean} isSassy - Whether to use sassy mode
 * @param {string} theme - Current theme name
 * @returns {string} Weather phrase with optional theme flair
 */
export function getWeatherPhrase(temp, condition, isSassy = false, theme = 'naval') {
  const style = isSassy ? 'sassy' : 'regular';
  const category = mapConditionToCategory(condition, temp);
  const phraseDb = weatherSayings[style];
  
  let possiblePhrases = [];

  // 1. Add theme-specific phrases for the exact condition (e.g., sassy.themes.bbq.sunny)
  if (phraseDb.themes[theme] && phraseDb.themes[theme][category]) {
    possiblePhrases.push(...phraseDb.themes[theme][category]);
  }

  // 2. Add general phrases for the exact condition (e.g., sassy.conditions.sunny)
  if (phraseDb.conditions[category]) {
    possiblePhrases.push(...phraseDb.conditions[category]);
  }

  // 3. Fallback: If we have very few specific phrases, add general theme phrases
  if (possiblePhrases.length < 5) {
    if (phraseDb.themes[theme] && phraseDb.themes[theme].general) {
      possiblePhrases.push(...phraseDb.themes[theme].general);
    }
  }
  
  // 4. Fallback: Add general "general" phrases
  if (possiblePhrases.length === 0) {
    possiblePhrases.push(...phraseDb.conditions.general);
  }

  // 5. Ultimate fallback
  if (possiblePhrases.length === 0) {
    possiblePhrases.push(isSassy ? "Weather is happening." : "Have a nice day!");
  }

  // 6. Select random phrase
  const phrase = possiblePhrases[Math.floor(Math.random() * possiblePhrases.length)];
  
  // 7. Add theme-specific flair
  return addThemeFlair(phrase, theme);
}

/**
 * Add theme-specific emoji flair to a phrase
 * @param {string} phrase - The weather phrase
 * @param {string} theme - Current theme name
 * @returns {string} Phrase with theme flair added
 */
function addThemeFlair(phrase, theme) {
	const flair = themeFlairs[theme] || '';
	return flair ? `${flair} ${phrase}` : phrase;
}
